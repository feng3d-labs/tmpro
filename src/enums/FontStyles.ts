/**
 * Styles to apply to the text such as Bold or Italic.
 */
export enum FontStyles
{
    Normal = 0x0,
    Bold = 0x1,
    Italic = 0x2,
    Underline = 0x4,
    LowerCase = 0x8,
    UpperCase = 0x10,
    SmallCaps = 0x20,
    Strikethrough = 0x40,
    Superscript = 0x80,
    Subscript = 0x100,
    Highlight = 0x200
}
